namespace Starnight.Entities;

/// <summary>
/// Enum holding the three possible Nitro subscription types
/// </summary>
public enum DiscordPremiumType
{
	/// <summary>
	/// No nitro subscription.
	/// </summary>
	None,

	/// <summary>
	/// Nitro Classic subscription.
	/// </summary>
	Classic,

	/// <summary>
	/// Full Nitro subscription.
	/// </summary>
	Nitro,

	/// <summary>
	/// Nitro Basic subscription.
	/// </summary>
	Basic
}
